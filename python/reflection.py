my_object = {1: "Hello, world!"}
# Получить тип обьекта
type(my_object)

# Проверить, является ли обьект экземпляром определенного класса
isinstance(my_object, MyClass)

# Получить список атрибутов объекта
dir(my_object)

# Получить значение атрибута объекта
getattr(my_object, "my_attribute")

# Установить значение атрибута объекта
setattr(my_object, "my_attribute", 123)
